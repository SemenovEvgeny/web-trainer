echo ""
echo "1. –ó–∞–≥—Ä—É–∑–∏—Ç–µ —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ –ø–∞–ø–∫–∏ 'dist' –Ω–∞ —Å–µ—Ä–≤–µ—Ä:"
